			 <dt>What does “Print on demand” Mean?</dt>
			 <dd>
				We only manufacture merchandise once purchased.
				Aka, once YOU sell it. No guessing which colors, sizes,
				or designs work best. Test your products!
				No inventory. No risk!
			 </dd>
			 <dt>How long do “print on demand” orders<br/>take to arrive to customers? </dt>
			 <dd>
				<strong class="fg-orange">USA</strong> 5 to 9 business days. <sup>(PRIORITY MAIL)</sup><br/>
				<strong class="fg-orange">Canada/Mexico</strong> 9 to 14 business days. <sup>(PRIORITY MAIL)</sup><br/>
				<strong class="fg-orange">International</strong> 14 to 21 business days. <sup>(PRIORITY MAIL)</sup>
			 </dd>
			 <dt>When to use “monthly plan” vs. “shopify app”? </dt>
			 <dd>
				<p><strong class="fg-orange">Monthly Plan</strong> - You’re a social influencer, athlete,
				   or musician, looking for passive income but don’t
				   have time to figure out logistics? Look no further!
				   Focus on what you do, we’ll handle the merch!
				   We provide your very own dedicated merch
				   team ready to deliver!
				</p>
				<p><strong class="fg-orange">Shopify App</strong> - Are you a designer that CAN handle
				   the workload? Do you have an existing merch line
				   on Shopify, but looking for higher profit margins?
				   Maybe you already have a strong team, but simply
				   looking for fulfillment? Look no further! Our “Print
				   On Demand” App is your best bet! 
				</p>
			 </dd>
			 <dt>How do payment break downs work?</dt>
			 <dd>
				You get 100% of the revenue share, once <strong class="fg-orange">cost of
				goods sold</strong> “COGS” (labor/production cost) have
				been recouped.
			 </dd>
			 <dt>When do I get paid?</dt>
			 <dd>
				<strong class="fg-orange">Monthly Plan</strong> - Monthly commisions. <br/>
				<strong class="fg-orange">Shopify App</strong> - Real time cost of goods sold “COGS”
				debiting + commision pay outs.
			 </dd>
			 <dt>What does “AD SPEND” Mean?</dt>
			 <dd>
				Monthly budget allocated to boosting ads and
				retargeting products to engaged fans. 100% of
				budget goes to platforms like Google, Facebook,
				Instagram &amp; Twitter.
			 </dd>
			 <dt>What does “DTG printing” mean?</dt>
			 <dd>
				<strong class="fg-orange">Direct to garmnet (DTG)</strong> - A method where printers
